// Menggunakan push
// Push adalah cara yang kita gunakan untuk menambahkan nilai ke dalam
// Array pada element terakhir. Misalnya kita memiliki Array seperti berikut:
const myFuckingArray = [1, 2, 3, 4, 5];

// Untuk menambahkan nilai ke dalam Array menggunakan method push, dapat
// dilakukan dengan cara menuliskan nama array-nya diikuti tanda
// titik dan method push berserta nilai yang ingin ditambahkan 
// yaitu 6 seperti berikut:
myFuckingArray.push(6);
console.log(myFuckingArray);

// Nila yang ditambahkan akan berada di element terakhir
